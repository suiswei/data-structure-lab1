F_num = int(input("Enter first number: "))
S_num = int(input("Enter second number: "))

if F_num < S_num:
    print(F_num + " is less than " + S_num)

elif F_num > S_num:
    print(F_num + " is greater than " + S_num)

else:
    print(F_num + " is equal to " + S_num)


